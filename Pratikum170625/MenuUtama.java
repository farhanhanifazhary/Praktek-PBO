    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        double makanan = Double.parseDouble(fieldHargaMakanan.getText());
        double minuman = Double.parseDouble(fieldHargaMinuman.getText());
        double totalHarga = makanan + minuman;
        double pajak = totalHarga * 0.1;
        double totalPembayaran = totalHarga + pajak;
        
        fieldTotalHarga.setText(Double.toString(totalHarga));
        fieldPajak.setText(Double.toString(pajak));
        fieldTotalPembayaran.setText(Double.toString(totalPembayaran));
    }//GEN-LAST:event_btnProsesActionPerformed

