    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        double totalPembayaran = Double.parseDouble(fieldTotalPembayaran.getText());
         try{
             double uangPembayaran = Double.parseDouble(fieldUangPembayaran.getText());
            double kembalian = uangPembayaran - totalPembayaran;
            String namaPelanggan = fieldPelanggan.getText();
            int noMeja = Integer.parseInt(fieldMeja.getText());
            if(kembalian < 0){
               JOptionPane.showMessageDialog(this, "Uangmu kurang", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(this, "Pesanan Akan Segera Diantarkan\n" + namaPelanggan + "\nMeja " + noMeja, "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
               fieldKembalian.setText(Double.toString(kembalian));
            }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input berupa angka", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBayarActionPerformed

